int		ft_isalnum(int c);

int		ft_strisalnum(char *s)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (ft_isalnum(s[i]) == 0 && s[i] != '-' && s[i] != '/')
			return (0);
		i++;
	}
	return (1);
}
